    //Method for calculating perimeter   
    private double perimeterretrieve(int i){
        double value;
        //Retrieves user input an assign them to variables
        a = Double.parseDouble(dimension1Input.getText());
        b = Double.parseDouble(dimension2Input.getText());
        c = Double.parseDouble(dimension3Input.getText());
        d = Double.parseDouble(dimension4Input.getText());
        e = Double.parseDouble(dimension5Input.getText());
        //Array with each formula to calculate perimter
        double[] perimeterformulas = {2*(a+b),2*Math.PI*a,a+c+d,(2*a*c)+(2*a*d)+(2*c*d),4*Math.PI*a*a,2*a*b+a*a,2*(a+b),a+b+d+e,Math.PI*a*(Math.sqrt(a*a+b*b))+Math.PI*a*a,2*Math.PI*a*a+2*Math.PI*a*b,a*c+d*c+e*c+b*a};
        //Calculates the perimeter of shape by using array and returns the value
        value = perimeterformulas[i];
        return value;
    }
